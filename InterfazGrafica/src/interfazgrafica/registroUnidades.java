
package interfazgrafica;

public class registroUnidades {
    String fecha;
    String idUnidad;
    int idOperador;
    String ultima_parada;
    String hora_ultima;
    String penultima_parada;
    String hora_penultima;
    String tiempo_transcurrido;
    String promedio_tiempo_transcurrido;
    String diferencia;

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getIdUnidad() {
        return idUnidad;
    }

    public void setIdUnidad(String idUnidad) {
        this.idUnidad = idUnidad;
    }

    public int getIdOperador() {
        return idOperador;
    }

    public void setIdOperador(int idOperador) {
        this.idOperador = idOperador;
    }

    public String getUltima_parada() {
        return ultima_parada;
    }

    public void setUltima_parada(String ultima_parada) {
        this.ultima_parada = ultima_parada;
    }

    public String getHora_ultima() {
        return hora_ultima;
    }

    public void setHora_ultima(String hora_ultima) {
        this.hora_ultima = hora_ultima;
    }

    public String getPenultima_parada() {
        return penultima_parada;
    }

    public void setPenultima_parada(String penultima_parada) {
        this.penultima_parada = penultima_parada;
    }

    public String getHora_penultima() {
        return hora_penultima;
    }

    public void setHora_penultima(String hora_penultima) {
        this.hora_penultima = hora_penultima;
    }

    public String getTiempo_transcurrido() {
        return tiempo_transcurrido;
    }

    public void setTiempo_transcurrido(String tiempo_transcurrido) {
        this.tiempo_transcurrido = tiempo_transcurrido;
    }

    public String getPromedio_tiempo_transcurrido() {
        return promedio_tiempo_transcurrido;
    }

    public void setPromedio_tiempo_transcurrido(String promedio_tiempo_transcurrido) {
        this.promedio_tiempo_transcurrido = promedio_tiempo_transcurrido;
    }

    public String getDiferencia() {
        return diferencia;
    }

    public void setDiferencia(String diferencia) {
        this.diferencia = diferencia;
    }
            
}
